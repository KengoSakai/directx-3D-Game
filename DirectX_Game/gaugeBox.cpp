/******************************************************************
初期化処理関数
*******************************************************************/
void CGauge::Initialize(void)
{
	Position.x = 400.0f;
	Position.y = 600.0f;
	Position.z = 0.0f;

	WidthHeight.x = NUM_Gauge;
	WidthHeight.y = 50.0f;
	WidthHeight.z = 0.0f;

	CScene2D::Initialize();
}

