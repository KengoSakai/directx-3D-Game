/******************************************************************
オブジェクト作成処理関数
*******************************************************************/
CGauge *CGauge::Create(void)
{
	CGauge *pGauge;

	//メモリの確保
	pGauge = new CGauge;

	//初期化処理
	pGauge->Initialize();

	//オブジェクトの型登録関数呼び出し
	pGauge->SetObjType(OBJTYPE_GAUGE);

	CManager::GetTextureManager()->LoadTexture(CTextureManager::TYPE_GAUGEBOX);

	//テクスチャを割り当てる
	pGauge->BindTexture(CManager::GetTextureManager()->GetTexture(CTextureManager::TYPE_GAUGEBOX));

	return pGauge;

}
